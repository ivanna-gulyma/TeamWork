
public class Student {
	public String sName = "";
    public int iGrade = 0;
    public int iAge = 0;
	
    public String getsName() {
		return sName;
	}
	public void setsName(String sName) {
		this.sName = sName;
	}
	public int getiGrade() {
		return iGrade;
	}
	public void setiGrade(int iGrade) {
		this.iGrade = iGrade;
	}
	public int getiAge() {
		return iAge;
	}
	public void setiAge(int iAge) {
		this.iAge = iAge;
	}
    
}
